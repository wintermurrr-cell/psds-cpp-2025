#include <iostream>  // Для вывода данных
#include <cmath>     // Для математических функций (квадратный корень)
#include <iomanip>   // Для управления точности вывода

using namespace std; // Подключаем стандартное пространство имен

// Функция для решения квадратного уравнения
void SolveQuadratic(int A, int B, int C) {
// Устанавливаем точность вывода чисел (6 знаков после запятой)
    cout << setprecision(6);

// Проверяем случай, когда уравнение становится линейным
    if (A == 0) {
// Проверяем случай, когда B тоже равно 0
        if (B == 0) {
// Если и A==0 и B==0, проверяем C
            if (C == 0) {
// 0==0 - бесконечное количество решений
                cout << "infinite solutions";
            } else {
// C!=0 при A==0 и B==0 - уравнение не имеет решений (например, 5==0)
                cout << "no solutions";
            }
        } else {
// A==0, но B!=0 - линейное уравнение B*x + C = 0
// Решение: x = -C/B
            double x = -static_cast<double>(C) / B;
            cout << x;
        }
        return;  // Завершаем функцию 
    }

// Вычисляем дискриминант для квадратного уравнения
// Формула: D = B^2 - 4*A*C
    double D = (static_cast<double>(B) * B) - (4.0 * A * C);

// Проверяем значение дискриминанта
    if (D > 0) {
// D > 0 - два различных вещественных корня
// Формулы: x1 = (-B + (корень из D)) / (2*A), 
//          x2 = (-B - (корень из D)) / (2*A)
        double x1 = (-B + sqrt(D)) / (2.0 * A);
        double x2 = (-B - sqrt(D)) / (2.0 * A);

// Убеждаемся, что x1 < x2 для корректного вывода
        if (x1 > x2) {
// Меняем местами, если нужно
            double temp = x1;
            x1 = x2;
            x2 = temp;
        }

// Выводим оба корня
        cout << x1 << " " << x2;
    }
    else if (D == 0) {
// D == 0 - один корень (два одинаковых)
// Формула: x = -B / (2*A)
        double x = -B / (2.0 * A);
        cout << x;
    }
    else {
// D < 0 - нет вещественных корней
        cout << "no solutions";
    }
}

